# include <stdio.h>

int sgetline (char *fp, char *buf)
{
    int i,
        c;

    i = 0;
    while ((c = *fp++) != '\n' && c != '\0')
    {
        buf[i++] = c;
    }
/**********
    if (i > 0 && buf[i-1] == '\r')
        i--;
***********/

    buf[i++] = '\n';

    return ((c == '\0') ? c : i);
}
